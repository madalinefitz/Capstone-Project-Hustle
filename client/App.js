function App(){

  const [jobCategories, setJobCategories] = useState([])

  useEffect(()=>{
    fetch('http://127.0.0.1:5555/jobcategories')
      .then(r=>r.json())
      .then(data => setJobCategories(data))
    }, [])
  

  

  return (
    <View style={styles.centeredView}>
      <SafeAreaView style={styles.container}>
      <FlatList
          data={jobCategories}
          keyExtractor={({id}) => id}
          renderItem={({item}) => (
            <Text>
              {item.category_name}
            </Text>
          )}
        />
    </SafeAreaView>
    </View>
  );
};

